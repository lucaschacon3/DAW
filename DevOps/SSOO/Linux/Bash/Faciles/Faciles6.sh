#!/bin/bash

#se requiere un argumento que sera el nombre del directorio
#donde estÃ¡n los ficheros los ficheros: ~/Desktop/$1
if [ $# -ne 3 ];then
	echo $0: se requieren tres parametros
	exit 1
fi	 

if [[ $1 = $2 && $2 = $3 ]];then
	echo "--->>>----------------------------------<<<---"
	echo "--->>> "'$1'":$1, "'$2'":$2 y "'$3'":$3 son iguales <<<---"
	echo "--->>>----------------------------------<<<---"
elif [[ $1 = $2 && $2 != $3 ]];then
	echo "--->>>--------------------------------<<<---"
	echo "--->>> Solo "'$1'":$1 y "'$2'":$2 son iguales <<<---"
	echo "--->>>--------------------------------<<<---"
elif [[ $1 = $3 && $2 != $3 ]];then
	echo "--->>>--------------------------------<<<---"
	echo "--->>> Solo "'$1'":$1 y "'$3'":$3 son iguales <<<---"
	echo "--->>>--------------------------------<<<---"
elif [[ $2 = $3 && $2 != $1 ]];then
	echo "--->>>--------------------------------<<<---"
	echo "--->>> Solo "'$2'":$2 y "'$3'":$3 son iguales <<<---"
	echo "--->>>--------------------------------<<<---"
else
	echo "--->>>---------------------------------------<<<---"
	echo "--->>> "'$1'":$1, "'$2'":$2 y "'$2'":$3 son diferentes <<<---"	
	echo "--->>>---------------------------------------<<<---"
fi